/**
 * 카테고리 생성을 위한 시스템 메시지 템플릿
 */
export const TAG_SYSTEM_PROMPT = TAG_PROMPT;

/**
 * 메모 내용을 기반으로 완성된 카테고리 프롬프트 생성
 * @param content - 메모 내용
 * @returns 완성된 프롬프트
 */
export function createTagPrompt(content: string): string {
  return `${TAG_PROMPT}

메모 내용:

${content}`;
}